package SwitchCase;

public class SwitchCase {
    public static void main(String[] args) {
        int s=2;
        switch(s) {
            case 1:
                System.out.println("Fan");
                break;
            case 2:
                System.out.println("Lamp");
                break;
            case 3:
                System.out.println("Bulb");
                break;
            case 4:
                System.out.println("TV");
                break;
            default:
                System.out.println("Not Applicable");
        }

        }
}
