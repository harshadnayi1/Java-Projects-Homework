package org.example;

public class App
{
    public static void main( String[] args )
    {
       int s=11;
       switch (s){
           case 1:
               System.out.println("fan");
               break;
           case 2:
               System.out.println("light");
               break;
           case 3:
               System.out.println("bulb");
               break;
           case 4:
               System.out.println("lamp");
               break;
           default:
               System.out.println("Not applicable");

       }

    }


}

